          width="200">
          <template scope="scope">
            <el-button
              size="small"
              type="info"
              @click="show(scope.$index,scope.row)">查看
            </el-button>
            <el-button
              size="small"
              type="warning"
              @click="edit(scope.$index, scope.row)">修改
            </el-button>

          </template>
        </el-table-column>
        <el-table-column
          prop="name"
