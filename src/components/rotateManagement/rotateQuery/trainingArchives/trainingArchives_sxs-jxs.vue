        <el-col :span="14"  align="right">
          <el-form-item label="姓名" prop="userName" >
            <el-input style="width:300px;"   v-model="formValidate.userName" placeholder="输入姓名搜索">
              <el-button @click="searchEvent"  slot="append"  icon="search"></el-button>
            </el-input>
          </el-form-item>
          <el-button :icon="searchMore ? 'arrow-down' : 'arrow-up'" @click="showSearchMore">高级查询</el-button>
        </el-col>
      </el-row>
      <!--高级搜索项-->
      <div v-if="searchMore" ref="searchMore" style="float: right">
        <el-form-item label="学校" prop="schoolName" >
          <el-input style="width:300px;"   v-model="formValidate.schoolName" placeholder="输入学校搜索">
            <el-button @click="searchEvent"  slot="append"  icon="search"></el-button>
          </el-input>
        </el-form-item>
        <el-button type="info" @click="searchEvent">查询</el-button>
      </div>
    </el-form>

    <!--列表数据-->
    <div>
      <!--表格数据-->
      <div
        id="myTable"
        ref="myTable"
      >
        <el-table
          align="center"
          :height="dynamicHt"
          :context="self"
          :data="tableData"
          tooltip-effect="dark"
          highlight-current-row
          style="width: 100%;height: 100%"
          @selection-change="handleSelectionChange">
          <el-table-column
            type="selection"
            width="55">
          </el-table-column>
          <el-table-column
            align="center"
            label="序号"
            prop="index"
            width="100">
            <template scope="scope">
              <span>{{scope.row.index}}</span>
            </template>
          </el-table-column>
          <el-table-column
            label="操作"
            width="80">
            <template scope="scope">
              <el-button size="small" @click="show(scope.row)">查看</el-button>
            </template>
          </el-table-column>
          <el-table-column
            show-overflow-tooltip
            align="center"
            prop="userName"
            label="姓名"
            width="200">
          </el-table-column>
          <el-table-column
            show-overflow-tooltip
            prop="schoolName"
            label="学校">
          </el-table-column>
          <!--<el-table-column-->
