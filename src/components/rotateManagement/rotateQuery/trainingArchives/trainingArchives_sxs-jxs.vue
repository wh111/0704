          <!--</el-table-column>-->

          <el-table-column
          show-overflow-tooltip
          prop="rotaryProclass"
          label="培训方向">
            <template scope="scope">
              {{['SXS','JXS'].includes(scope.row.studentTypes)?scope.row.specialty:scope.row.rotaryProclass}}
            </template>
          </el-table-column>
          <el-table-column
            show-overflow-tooltip
            prop="mobile"
            label="手机号">
          </el-table-column>
          <el-table-column
